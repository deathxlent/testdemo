    return {
        setLang: function(lang) {
            currentLang = lang;
            localStorage.setItem('app-lang', lang);
            this.applyTranslations();
        },
        
        getLang: function() {
            return currentLang;
        },
        
        t: function(key) {
            return translations[currentLang][key] || translations['zh'][key] || key;
        },
        
        applyTranslations: function() {
            document.querySelectorAll('[data-i18n]').forEach(function(el) {
                var key = el.getAttribute('data-i18n');
                var text = translations[currentLang][key];
                if (text) {
                    el.textContent = text;
                }
            });
            
            document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
                var key = el.getAttribute('data-i18n-placeholder');
                var text = translations[currentLang][key];
                if (text) {
                    el.placeholder = text;
                }
            });
            
            document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
                var key = el.getAttribute('data-i18n-title');
                var text = translations[currentLang][key];
                if (text) {
                    el.title = text;
                }
            });
            
            document.querySelectorAll('[data-i18n-tip]').forEach(function(el) {
                var key = el.getAttribute('data-i18n-tip');
                var text = translations[currentLang][key];
                if (text) {
                    el.setAttribute('data-tip', text);
                }
            });
            
            // Update language button
            var langBtn = document.getElementById('langSwitchBtn');
            if (langBtn) {
                langBtn.textContent = translations[currentLang]['lang.switch'];
            }
        },
        
        init: function() {
            var savedLang = localStorage.getItem('app-lang');
            if (savedLang && translations[savedLang]) {
                currentLang = savedLang;
            }
        }
    };
